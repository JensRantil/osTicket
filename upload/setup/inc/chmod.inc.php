<h1>Configuration file permission settings</h1><br>
<p>osTicket installer requires ability to write to the configuration file <strong>include/ost-config.php</strong>. Please follow the instructions below to give read and write access to the web server.</p>
<h4>Shh/Telnet</h4>
<p>chmod 777 include/ost-config.php</p>
<h4>FTP</h4>
<p>Using WS_FTP this would be right hand clicking on the file ost-config.php , selecting chmod, and then giving all permissions to the file.</p>
<h4>Cpanel</h4>
<p>Click on the file, select change permission, and then giving all permissions to the file.</p>
